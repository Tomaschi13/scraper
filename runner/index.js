async function createBridgePage(context, extensionId) {
  const page = await context.newPage();
  await page.goto(`chrome-extension://${extensionId}/runner/bridge.html`);
  await page.waitForFunction(() => Boolean(globalThis.scraperRunnerBridge));
  return page;
}

async function callBridge(page, method, payload) {
  return page.evaluate(async ({ bridgeMethod, bridgePayload }) => {
    const bridge = globalThis.scraperRunnerBridge;
    if (!bridge || typeof bridge[bridgeMethod] !== "function") {
      throw new Error(`Bridge method is unavailable: ${bridgeMethod}`);
    }

    return bridge[bridgeMethod](bridgePayload);
  }, {
    bridgeMethod: method,
    bridgePayload: payload
  });
}

function shouldBlockRequestForBandwidth(url, resourceType) {
  let parsed;
  try {
    parsed = new URL(url);
  } catch (_err) {
    return false;
  }

  if (RESOURCE_BLOCKER_SCHEME_ALLOWLIST.includes(parsed.protocol)) {
    return false;
  }

  if (BLOCKED_RESOURCE_TYPES.has(resourceType)) {
    return true;
  }

  const hostname = parsed.hostname.toLowerCase();
  const haystack = `${hostname}${parsed.pathname.toLowerCase()}`;
  return BLOCKED_HOST_PATTERNS.some((pattern) => haystack.includes(pattern));
}

async function installAggressiveResourceBlocker(context) {
  await context.route("**/*", (route, request) => {
    if (shouldBlockRequestForBandwidth(request.url(), request.resourceType())) {
      route.abort().catch(() => { /* request already settled */ });
      return;
    }
    route.continue().catch(() => { /* request already settled */ });
  });
}

